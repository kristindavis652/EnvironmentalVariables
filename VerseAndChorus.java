public class VerseAndChorus {
    public static void thisOldMan()
    {
        System.out.println("With a knick-knack paddywhack,");
        System.out.println("Give the dog a bone,");
        System.out.println("This old man came rolling home.");
        System.out.println(" ");
    }

    public static void main(String[] args) {
        System.out.println("This old man, he played one.");
        System.out.println("He played knick-knack on my thumb;");
        thisOldMan();

        System.out.println("This old man, he played two.");
        System.out.println("He played knick-knack on my shoe;");
        thisOldMan();

        System.out.println("This old man, he played three.");
        System.out.println("He played knick-knack on my knee;");
        thisOldMan();

        System.out.println("This old man, he played four.");
        System.out.println("He played knick-knack on my door;");
        thisOldMan();

        System.out.println("This old man, he played five.");
        System.out.println("He played knick-knack on my hive;");
        thisOldMan();

        System.out.println("This old man, he played six.");
        System.out.println("He played knick-knack on my sticks;");
        thisOldMan();

    }
}
